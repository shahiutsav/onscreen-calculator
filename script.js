const add = function () {
    return arguments[0] + arguments[1]
}

console.log(add(1, 2))

const subtract = function () {
    return arguments[0] - arguments[1]
}
console.log(subtract(2, 1))

const multiply = function () {
    return arguments[0] * arguments[1]
}

console.log(multiply(2, 3))

const divide = function () {
    return arguments[0] / arguments[1]
}

console.log(divide(4, 2))
